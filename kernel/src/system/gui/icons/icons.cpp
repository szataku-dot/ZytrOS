bool is_mouse_over_start(int mouse_x, int mouse_y) {
    if (mouse_x >= start_button.x1 && mouse_x <= start_button.x2 &&
        mouse_y >= start_button.y1 && mouse_y <= start_button.y2) {
        return true;
    }
    return false;
}