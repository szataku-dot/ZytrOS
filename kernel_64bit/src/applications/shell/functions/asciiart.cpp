void print_ascii_art(const char* text)
{
    int len = strlen(text);

    for(int row = 0; row < 5; row++)
    {
        for(int i = 0; i < len; i++)
        {
            char c = text[i];

            // lover case letters -> upper case letters
            if(c >= 'a' && c <= 'z')
                c -= 32;

            int index = -1;

            // A-Z
            if(c >= 'A' && c <= 'Z')
            {
                index = c - 'A';
            }

            // 0-9
            else if(c >= '0' && c <= '9')
            {
                index = 26 + (c - '0');
            }

            // special chars
            else
            {
                switch(c)
                {
                    case '!': index = 36; break;
                    case '@': index = 37; break;
                    case '#': index = 38; break;
                    case '$': index = 39; break;
                    case '%': index = 40; break;
                    case '&': index = 41; break;
                    case '?': index = 42; break;
                    case ':': index = 43; break;
                    case ';': index = 44; break;
                    case '+': index = 45; break;
                    case '-': index = 46; break;
                    case '=': index = 47; break;
                }
            }

            if(index >= 0)
                print(font[index][row]);
            else
                print("     ");

            print(" ");
        }

        print("\n");
    }
}